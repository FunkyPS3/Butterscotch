static float clampf_local(float v, float min, float max)
{
    if (v < min)
        return min;
    if (v > max)
        return max;
    return v;
}

static int clampi_local(int v, int min, int max)
{
    if (v < min)
        return min;
    if (v > max)
        return max;
    return v;
}

static void fill_rect_argb8888(uint32_t *pixels,
                               int surface_w,
                               int surface_h,
                               int x,
                               int y,
                               int w,
                               int h,
                               uint32_t color)
{
    if (!pixels || w <= 0 || h <= 0)
        return;

    int x0 = clampi_local(x, 0, surface_w);
    int y0 = clampi_local(y, 0, surface_h);
    int x1 = clampi_local(x + w, 0, surface_w);
    int y1 = clampi_local(y + h, 0, surface_h);

    if (x1 <= x0 || y1 <= y0)
        return;

    for (int py = y0; py < y1; py++)
    {
        uint32_t *row = pixels + (py * surface_w);
        for (int px = x0; px < x1; px++)
        {
            row[px] = color;
        }
    }
}

static void draw_char_8x8(uint32_t *pixels,
                          int surface_w,
                          int surface_h,
                          int x,
                          int y,
                          char c,
                          uint32_t color);

static void draw_text_8x8(uint32_t *pixels,
                          int surface_w,
                          int surface_h,
                          int x,
                          int y,
                          const char *text,
                          uint32_t color);

static void put_pixel_argb8888(uint32_t *pixels,
                               int surface_w,
                               int surface_h,
                               int x,
                               int y,
                               uint32_t color)
{
    if (!pixels)
        return;
    if (x < 0 || y < 0 || x >= surface_w || y >= surface_h)
        return;
    pixels[y * surface_w + x] = color;
}

