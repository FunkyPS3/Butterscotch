int LoadingScreen_init(LoadingScreen *loading, const char *game_name)
{
    if (!loading)
        return 0;

    loading->game_name = game_name ? game_name : "JOGO";
    loading->title_text = "Carregando...";
    loading->status_text = "Inicializando";
    loading->progress = 0.0f;
    loading->ready = 1;
    return 1;
}

void LoadingScreen_setStatus(LoadingScreen *loading,
                                const char *status_text,
                                float progress)
{
    if (!loading || !loading->ready)
        return;

    loading->status_text = status_text ? status_text : "Carregando";
    loading->progress = clampf_local(progress, 0.0f, 1.0f);
}

static void draw_char_8x8(uint32_t *pixels,
                          int surface_w,
                          int surface_h,
                          int x,
                          int y,
                          char c,
                          uint32_t color)
{
    const uint8_t *glyph = font8x8_basic[(unsigned char)c];

    for (int row = 0; row < 8; row++)
    {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++)
        {
            if (bits & (1 << (7 - col)))
            {
                put_pixel_argb8888(pixels, surface_w, surface_h, x + col, y + row, color);
            }
        }
    }
}

static void draw_text_8x8(uint32_t *pixels,
                          int surface_w,
                          int surface_h,
                          int x,
                          int y,
                          const char *text,
                          uint32_t color)
{
    if (!pixels || !text)
        return;

    int pen_x = x;
    int pen_y = y;

    for (const char *p = text; *p; p++)
    {
        if (*p == '\n')
        {
            pen_x = x;
            pen_y += 8;
            continue;
        }

        draw_char_8x8(pixels, surface_w, surface_h, pen_x, pen_y, *p, color);
        pen_x += 8;
    }
}

void LoadingScreen_render(LoadingScreen *loading,
                             gcmContextData *context,
                             rsxBuffer *buffer,
                             uint16_t width,
                             uint16_t height)
{
    if (!loading || !loading->ready || !buffer || !buffer->ptr)
        return;

    const int panel_w = 560;
    const int panel_h = 160;
    const int panel_x = ((int)width - panel_w) / 2;
    const int panel_y = ((int)height - panel_h) / 2;

    const int bar_x = panel_x + 24;
    const int bar_y = panel_y + 108;
    const int bar_w = panel_w - 48;
    const int bar_h = 18;

    const int fill_w = clampi_local((int)(bar_w * loading->progress), 0, bar_w);
    int credit_x = 8;
    int credit_y = height - 48;
    int gap = 12;

    uint32_t *pixels = (uint32_t *)buffer->ptr;

    /*
     * Seu main já usa clearBuffer(&buffers[currentBuffer], color)
     * para limpar framebuffers antes de desenhar :contentReference[oaicite:1]{index=1}
     */
    clearBuffer(buffer, 0xFF101010u);

    fill_rect_argb8888(pixels, width, height, panel_x, panel_y, panel_w, panel_h, RGBA(0, 0, 0, 0));

    fill_rect_argb8888(pixels, width, height,
                       panel_x, panel_y,
                       panel_w, panel_h,
                       RGBA(0, 0, 0, 0.67));

    draw_text_8x8(pixels, width, height,
                  panel_x + 24, panel_y + 24,
                  loading->game_name,
                  RGBA(0, 168, 255, 255));

    draw_text_8x8(pixels, width, height,
                  panel_x + 24, panel_y + 52,
                  loading->title_text,
                  RGBA(208, 208, 208, 255));

    draw_text_8x8(pixels, width, height,
                  panel_x + 24, panel_y + 80,
                  loading->status_text,
                  RGBA(176, 176, 176, 255));

    const char *credit_line1 = "butterscotch was originally created by MrPowerGamerBR (https://mrpowergamerbr.com)";
    const char *credit_line2 = "Ported to PlayStation 3 by WinG4merBR (https://github.com/WinG4merBR)";

    draw_text_8x8(pixels, width, height,
                  credit_x, credit_y,
                  credit_line1,
                  RGBA(255, 255, 255, 255));

    draw_text_8x8(pixels, width, height,
                  credit_x, credit_y + gap,
                  credit_line2,
                  RGBA(255, 255, 255, 255));

    fill_rect_argb8888(pixels, width, height,
                       bar_x, bar_y,
                       bar_w, bar_h,
                       RGBA(64, 64, 64, 255));

    fill_rect_argb8888(pixels, width, height,
                       bar_x, bar_y,
                       fill_w, bar_h,
                       RGBA(255, 85, 85, 255));
}

void LoadingScreen_shutdown(LoadingScreen *loading)
{
    if (!loading)
        return;
    loading->ready = 0;
}